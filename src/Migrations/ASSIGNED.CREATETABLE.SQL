

CREATE TABLE Assigned(
    "TID" uuid NOT NULL,
    "ASSIGNED_UID" uuid NOT NULL,
    "STATUS" task_status DEFAULT 'TODO',  -- ✅ here
    "CREATED_AT" TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    "UPDATED_AT" TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,

    PRIMARY KEY ("TID", "ASSIGNED_UID"), -- 🔥 important

    FOREIGN KEY ("TID") REFERENCES task("TID") ON DELETE CASCADE,
    FOREIGN KEY ("ASSIGNED_UID") REFERENCES users("UID") ON DELETE CASCADE
);